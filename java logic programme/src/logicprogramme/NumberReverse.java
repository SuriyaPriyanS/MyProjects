package logicprogramme;

public class NumberReverse {
	public static void main(String[] args) {
		
		int number =123456;
		int rev=0;
		

		
		for(;number!=0;number=number/10) {
			int remainder=number%10;
			rev=rev*10+remainder;
		}
		System.out.println(rev);
		
	}

}

